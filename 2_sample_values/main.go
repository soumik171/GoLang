package main

import "fmt"

func main() {

	// simple values/int
	fmt.Println(1 + 1)

	// string
	fmt.Println("Hello everyone")

	// bool
	fmt.Println(true)
	fmt.Println(false)

	// float
	fmt.Println(10.6)
	fmt.Println(4 / 3)     // get decimal values
	fmt.Println(4.0 / 3.0) // get fractional values
}
